package com.vtech;

public class Customer {

	private String name;
	private long mobile;
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setMobile(long mobile) {
		this.mobile = mobile;
	}
	
	public String getName() {
		return this.name;
	}
	
	public long getMobile() {
		return this.mobile;
	}
}
