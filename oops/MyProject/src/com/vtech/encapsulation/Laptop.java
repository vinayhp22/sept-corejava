package com.vtech.encapsulation;

public class Laptop {

	private String color;
	private int ram;
	
	public void setColor(String color) {
		this.color = color;
	}
	
	public void setRam(int ram) {
		this.ram = ram;
	}
	
	public String getColor() {
		return this.color;
	}
	
	public int getRam() {
		return this.ram;
	}
}
